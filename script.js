function cariNilai() {
    const inputNama = document.getElementById('nama').value;
    const inputKelas = document.getElementById('kelas').value;
    const resultElement = document.getElementById('result');

    const student = studentData.find(s => s.nama === inputNama && s.kelas === inputKelas);

    if (student) {
        resultElement.innerHTML = `<p>${inputNama}</p> 
        <p>kelas ${inputKelas}</p> 
        <p>Status Nilai: ${student.status}</p>`;
    } else {
        resultElement.innerHTML = `<p>Data tidak ditemukan</p>`;
    }
}
